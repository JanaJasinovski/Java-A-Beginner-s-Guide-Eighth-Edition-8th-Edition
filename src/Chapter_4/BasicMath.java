package Chapter_4;

public class BasicMath {
    public static void main(String[] args) {
        int a = 1 + 1;
        int b = a * 3;
        int c = b / 4;
        int d = c - a;
        int e = -d;
        System.out.println(a + "\n" + b + "\n" + c + "\n" + d + "\n" + e);

        double da = 1 + 1;
        double db = da * 3;
        double dc = b / 4;
        double dd = c - a;
        double de = -d;
        System.out.println(da + "\n" + db + "\n" + dc + "\n" + dd + "\n" + de);
    }
}
