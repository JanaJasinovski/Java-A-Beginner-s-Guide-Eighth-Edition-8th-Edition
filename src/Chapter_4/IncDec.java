package Chapter_4;

public class IncDec {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        int c, d;
        c = ++b;
        d = a++;
        c++;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
